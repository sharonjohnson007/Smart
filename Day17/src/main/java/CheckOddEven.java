package main.java;

public class CheckOddEven {
    public static void main(String[] args) {
        int number = -2;
        System.out.println("The number is " + number);
        if (number % 2 == 0) {
            System.out.println("It is an even number");
        } else {
            System.out.println("It is an odd number");
        }
        System.out.println("Bye!!");
    }
}

